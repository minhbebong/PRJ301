package model;

import java.sql.Date;

/**
 *
 * @author Lenovo
 */
public class Article {
    int id;
    String title;
    Date publicDate;

    public Article(int id, String title, Date publicDate) {
        this.id = id;
        this.title = title;
        this.publicDate = publicDate;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public Date getPublicDate() {
        return publicDate;
    }

    public void setPublicDate(Date publicDate) {
        this.publicDate = publicDate;
    }
    
}
